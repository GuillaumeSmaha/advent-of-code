package p1

import "testing"

func TestSum(t *testing.T) {
	tests := []struct {
		Input  string
		Result int
	}{
		{
			Input:  "1122",
			Result: 3,
		},
		{
			Input:  "1111",
			Result: 4,
		},
		{
			Input:  "1234",
			Result: 0,
		},
		{
			Input:  "91212129",
			Result: 9,
		},
		{
			Input:  "@a.txt",
			Result: 995,
		},
	}

	for i, test := range tests {
		n := Sum(test.Input)
		if n != test.Result {
			t.Fatalf("a.%d: %s: %d (should be %d)", i, test.Input, n, test.Result)
		}
	}
}

